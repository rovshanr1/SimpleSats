import UIKit

extension UIFont {
    static func boldLivvic(selfSize: CGFloat) -> UIFont {
        UIFont(name: "Livvic-Bold", size: selfSize)!
    }
    
    static func semiBoldLivvic(selfSize: CGFloat) -> UIFont {
        UIFont(name: "Livvic-SemiBold", size: selfSize)!
    }
    
    static func regularLivvic(selfSize: CGFloat) -> UIFont {
        UIFont(name: "Livvic-Regular", size: selfSize)!
    }
    
    static func mediumLivvic(selfSize: CGFloat) -> UIFont {
        UIFont(name: "Livvic-Medium", size: selfSize)!
    }
    
    static func lightLivvic(selfSize: CGFloat) -> UIFont {
        UIFont(name: "Livvic-Light", size: selfSize)!
    }
    
    
}
